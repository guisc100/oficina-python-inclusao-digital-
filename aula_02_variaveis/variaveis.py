# Oficina de Introdução à Programação em Python
# Aula 02 - Variáveis

# Variáveis são usadas para armazenar informações

# Exemplo 1: Variáveis do tipo texto (string)
nome = "Guilherme"
cidade = "Florianópolis"

print("Nome:", nome)
print("Cidade:", cidade)

# Exemplo 2: Variáveis numéricas
idade = 30
altura = 1.77

print("Idade:", idade)
print("Altura:", altura)

# Exemplo 3: Operações com variáveis numéricas
ano_atual = 2026
ano_nascimento = ano_atual - idade

print("Ano de nascimento:", ano_nascimento)

# Exemplo 4: Atualizando o valor de uma variável
idade = idade + 1
print("Idade no próximo ano:", idade)

print("Fim da Aula 02 - Variáveis")
