# Oficina de Introdução à Programação em Python
# Aula 03 - Condicionais (if / else)

# Exemplo 1: Verificação simples
idade = 18

if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")

# Exemplo 2: Comparação de números
nota = 7

if nota >= 6:
    print("Aluno aprovado.")
else:
    print("Aluno reprovado.")

# Exemplo 3: Uso de elif
hora = 14

if hora < 12:
    print("Bom dia!")
elif hora < 18:
    print("Boa tarde!")
else:
    print("Boa noite!")

print("Fim da Aula 03 - Condicionais")
