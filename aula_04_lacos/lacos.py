# Oficina de Introdução à Programação em Python
# Aula 04 - Laços de repetição (for / while)

# Exemplo 1: Laço for com range
print("Contando de 1 a 5:")
for numero in range(1, 6):
    print(numero)

# Exemplo 2: Laço for percorrendo uma lista
nomes = ["Ana", "João", "Maria"]

print("\nLista de nomes:")
for nome in nomes:
    print(nome)

# Exemplo 3: Laço while
contador = 1

print("\nContagem com while:")
while contador <= 5:
    print(contador)
    contador += 1

print("Fim da Aula 04 - Laços")
